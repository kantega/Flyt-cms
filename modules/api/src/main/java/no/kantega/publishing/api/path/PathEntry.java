package no.kantega.publishing.api.path;

/**
 * Represents an entry in the a page's path, i.e. an ancestor.
 */
public class PathEntry {
    private int id = 0;
    private String title = "";
    private int contentTemplateId = 0;

    public PathEntry(int id, String title) {
        this.id = id;
        this.title = title;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getContentTemplateId() {
        return contentTemplateId;
    }

    public void setContentTemplateId(int contentTemplateId) {
        this.contentTemplateId = contentTemplateId;
    }
}
